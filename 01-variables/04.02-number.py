# NUMBER
numero_int = -10 * 20  # enteros positivos o negativos
numero_float = 3.14

# Divisiones
# / → Siempre devuelve float.
# // → Devuelve un int, descartando la parte decimal.
div = 10 / 2
print("10/2 = ", div)
division_int = 9 // 2
division_float = 9 / 2
print("9/2 int = ", division_int, ", 9/2 float = ", division_float)

# Residuo (%)
residuo = 9 % 2
print("9%2 = ", residuo)

# Elevar a la potencia (**)
print("2^3 = ", 2**3)

# Redondear (round(valor, numero_decimales))
